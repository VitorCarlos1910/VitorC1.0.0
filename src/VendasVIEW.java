    private void listarProdutos() {
        ProdutosDAO prod = new ProdutosDAO();
        List<ProdutosDTO> pro = (List<ProdutosDTO>) prod.listarProdutosVendidos();

        DefaultTableModel tab = (DefaultTableModel) tabela.getModel();
        tab.setNumRows(0);

        for (ProdutosDTO a : pro) {
            tab.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getValor(),
                a.getStatus()
            });
        }
    }

